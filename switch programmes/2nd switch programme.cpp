#include <iostream>
using namespace std; 
int main ()
{ 
 int num; 
 cout<<"Enter a number:"; 
 cin>>num; 
 switch (num) 
 { 
 case 20: 
 cout<<"It is 20"; 
 break; 
 case 30: 
 cout<<"It is 30"; 
 break; 
 case 40: 
 cout<<"It is 40"; 
 break; 
 default: 
 cout<<"Not 20, 30 or 40"; 
 } 
 return 0;
} 
